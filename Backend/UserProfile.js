class UserProfile {

    constructor(id, firstName, lastName, email, phoneNumber, dob, gender, password, address) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.dob = dob;
        this.gender = gender;
        this.password = password;
        this.address = address;
    }
}